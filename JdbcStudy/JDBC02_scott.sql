--○데이터 잘라내기
TRUNCATE TABLE TBL_MEMBER;


--시퀀스 생선
CREATE SEQUENCE MEMBERSEQ
NOCACHE;

--○조회
SELECT *
FROM TBL_MEMBER;


--○데이터 입력 쿼리문 준비
INSERT INTO TBL_MEMBER(SID, NAME, TEL) VALUES(MEMBERSEQ.NEXTVAL, '조영관', '010-1111-1111')
;



--인원 수 파악 쿼리문 준비
SELECT COUNT(*) AS COUNT
FROM TBL_MEMBER;

SELECT COUNT(*) AS COUNT FROM TBL_MEMBER
;


